 void sh1106_Char(uint8_t col, uint8_t line, int chr) {
    uint8_t *pdata; 
    if((pdata = (uint8_t *)malloc(54)) == NULL){
        // error just return atm
        return;
    } 
    
    int charindx = chr * 20;
    uint8_t page = linepos[line];
    uint8_t collow = colpos[col] & 0x0F;
    int8_t colhigh = (colpos[col] >> 4) | 0x10;
    int i = 0;

    *(pdata + i) = 0x80;
    i++;
    *(pdata + i) = 0xB0 + page; //select page 0 - 7
    i++;
    *(pdata + i) = 0x80;
    i++;
    *(pdata + i) = collow;
    i++;
    *(pdata + i) = 0x80;
    i++;
    *(pdata + i) = colhigh;
    i++;
    int j;
    for (j = 0; j < 10; j++) {
        *(pdata + i) = 0xC0;
        i++;
        *(pdata + i) = chset2[charindx];
        i++;
        charindx++;
    }

    *(pdata + i) = 0x80;
    i++;
    *(pdata + i) = 0xB0 + page + 1; //select page 0 - 7
    i++;
    *(pdata + i) = 0x80;
    i++;
    *(pdata + i) = collow;
    i++;
    *(pdata + i) = 0x80;
    i++;
    *(pdata + i) = colhigh;
    i++;
    for (j = 0; j < 9; j++) {
        *(pdata + i) = 0xC0;
        i++;
        *(pdata + i) = chset2[charindx];
        i++;
        charindx++;
    }
    *(pdata + i) = 0x40;
    i++;
    *(pdata + i) = chset2[charindx];
    i++;
    charindx++;    
    
    //while(i2c_Ready() != I2C_STATUS_IDLE);
   
    i2c_Write(pdata, i);
   
 }
 
 void display_Value(int16_t val){
     bool negf;
     uint8_t pos= 6;
     
     if(val < 0){
         negf = true;
         val = (~val + 1);
     }
     else{
         negf = false;
     }
     if(val == 0){
         sh1106_Char(1, 1, 0);
         sh1106_Char(2, 1, 0);
         sh1106_Char(3, 1, 0);
         sh1106_Char(4, 1, 0);
         sh1106_Char(5, 1, 0);
         sh1106_Char(6, 1, 10);         
         return;
     }  
     int dig;
     while(val != 0){
     
        dig = val % 10;
        if(dig == 0)
            dig = 10;
        sh1106_Char(pos, 1, dig);
         
        val = val / 10;
        pos--;           
         
     } 
     if(negf == true){
         sh1106_Char(pos, 1, 11);
         pos--;
     }     
     for(; pos > 0 ; pos--){
         sh1106_Char(pos, 1, 0);
     }          
 }
 
